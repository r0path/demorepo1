/**
 * Sanitize user input to prevent XSS attacks.
 *
 * @param string $input The user input to sanitize.
 * @return string The sanitized input.
 */
function sanitize_input($input) {
    return htmlspecialchars($input, ENT_QUOTES, 'UTF-8');
}

